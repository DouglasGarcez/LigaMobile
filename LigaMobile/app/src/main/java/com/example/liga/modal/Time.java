package com.example.liga.modal;

public class Time {

    private int time_id;

    private String time_imagem;

    private String time_nome;

    public int getTime_id() {
        return time_id;
    }

    public void setTime_id(int time_id) {
        this.time_id = time_id;
    }

    public String getTime_imagem() {
        return time_imagem;
    }

    public void setTime_imagem(String time_imagem) {
        this.time_imagem = time_imagem;
    }

    public String getTime_nome() {
        return time_nome;
    }

    public void setTime_nome(String time_nome) {
        this.time_nome = time_nome;
    }
}
