package com.example.liga.modal;

public class Liga {

    private int liga_id;
    private int liga_numero;
    private String liga_nome;

    public int getLiga_id() {
        return liga_id;
    }

    public void setLiga_id(int liga_id) {
        this.liga_id = liga_id;
    }

    public int getLiga_numero() {
        return liga_numero;
    }

    public void setLiga_numero(int liga_numero) {
        this.liga_numero = liga_numero;
    }

    public String getLiga_nome() {
        return liga_nome;
    }

    public void setLiga_nome(String liga_nome) {
        this.liga_nome = liga_nome;
    }
}
